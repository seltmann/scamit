<h2>SCAMIT's Taxonomic Publication Support Policy </h2>

<span class='stdsize'>
<ol>

<li>The officers will decide at the beginning of each grant season whether
there are sufficient funds to offer grants. The amount available for
any grant season will not exceed 25% of the total moneys in the SCAMIT
accounts as of the first day of the grant season. The available amount
will be determined by the Treasurer. This policy is subject to change
at the discretion of the duly elected officers.</li>

<li>If money is available, applications for publication support will be
accepted between June 1st and December 1st of each year. An
announcement to this affect will be posted on the SCAMIT web site.</li>

<li>Accepted grants will be announced and the disbursement of funds will
be completed prior to April 30th of the following year.</li>

<li>Support will be limited to publications that are pertinent to the
purpose and goals of the Association as outlined in Article 2 of the
constitution.</li>

<li>Disbursement of funds directly to the party or parties providing the
professional services is preferred.</li>

<li>Funds shall only be granted in support of costs associated with the
completion of works intended for publication in peer-reviewed
journals.</li>

<li>Funds shall be offered for the defraying of costs to prepare a
manuscript (e.g. typing, text processing), illustrations
(e.g. drawings, photos, SEM) and journal page charges.</li>

<li>No support will be provided for travel, time, or any costs incurred in
the development or conduct of the research leading to the preparation
of the manuscript.</li>

<li>Authors receiving SCAMIT support shall agree to acknowledge that
support in the publication.</li>

<li>Support is provided only to members in good standing.</li>

</ol>
</span>

